import java.lang.*;
import java.io.*;

public class Arithmetic {
    public static void main(String args[])
    {
        int a=14, b=5;

        int q = a/b;
        int r = a%b;

        float qu = (float)a/b;

        System.out.println(q);
        System.out.println(r);
        System.out.println(qu);
    }
}