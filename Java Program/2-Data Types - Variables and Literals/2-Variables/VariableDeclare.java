import java.lang.*;

public class VariableDeclare 
{
    public static void main(String arg[])
    {
        byte b = 127;
        short s = 300;
        int i = 100;
        float f = 25.3f;
        char c = 'A';
        
        System.out.println(b);
        System.out.println(s);
        System.out.println(i);
        System.out.println(f);
        System.out.println(c);
    }
}
